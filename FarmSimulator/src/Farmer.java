public class Farmer {
	
	private String name;
	private int age;
	private String skill;
	
	public Farmer(String farmerName, int farmerAge, String farmerSkill) {
		name = farmerName;
		age = farmerAge;
		skill = farmerSkill;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public String getSkill() {
		return skill;
	}
}

	